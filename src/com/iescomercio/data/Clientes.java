    private void jButtonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAnteriorActionPerformed
        // boton que muestra el cliente anterior de la coleccion en funcion a un indice
        try {
            if (indice < 1) {
                indice = 0;
            } else {
                indice--;
            }
            Cliente c = listaClientes.recuperarCliente(indice);
            this.recuperarDatos(c);
            //jLabelInfoCliente.setText("Cliente nº: " + indice);
        } catch (IndexOutOfBoundsException ex) {
            v.setMensaje("Acceso a datos incorrecto", ex);
        }
    }//GEN-LAST:event_jButtonAnteriorActionPerformed

    private void jButtonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSiguienteActionPerformed
        // boton que muestra el cliente siguiente de la coleccion en funcion a un indice
        try {
            if (indice >= listaClientes.recuperarLongitud() - 1) {
                indice = listaClientes.recuperarLongitud() - 1;
            } else {
                indice++;
            }
            Cliente c = listaClientes.recuperarCliente(indice);
            this.recuperarDatos(c);
            //jLabelInfoCliente.setText("Cliente nº: " + indice);
        } catch (IndexOutOfBoundsException ex) {
            v.setMensaje("Acceso a datos incorrecto", ex);
        }
    }//GEN-LAST:event_jButtonSiguienteActionPerformed

    private void formInternalFrameClosed(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameClosed
        // metodo que escribe el fichero al cerrar la ventana
        try {
            DataOutputStream dos = new DataOutputStream(new FileOutputStream("f:\\testdata\\clientes.txt"));
            // inicio bucle = 0
            // final bucle = la longitud de la coleccion
            for (int i = 0; i < listaClientes.recuperarLongitud(); i++) {
                Cliente c;
                // recupero la informacion de la coleccion en base a un indice que le paso
                c = listaClientes.recuperarCliente(i);
                // escribo el fichero bloque a bloque
                // cada bloque es un string con la informacion de cada elemento del formulario
                dos.writeUTF(c.getCodigo());
                dos.writeUTF(c.getDni());
                dos.writeUTF(c.getNombre());
                dos.writeUTF(c.getApellidos());
                dos.writeUTF(Integer.toString(c.getEstadoCivil()));
                dos.writeUTF(Boolean.toString(c.isSocio()));
                dos.writeUTF(c.getDireccion());
                dos.writeUTF(Integer.toString(c.getTelefonoFijo()));
                dos.writeUTF(Integer.toString(c.getTelefonoMovil()));
                dos.writeUTF(Integer.toString(c.getEdad()));
                // escribo un salto de linea para separar clientes en el fichero
                dos.writeUTF("\n");
            }
            dos.close();
        } catch (FileNotFoundException ex) {
            v.setMensaje("Fichero no encontrado", ex);
            //jLabelInfoException.setText("FileNotFoundException");
        } catch (IOException ex) {
            v.setMensaje("Error de entrada salida", ex);
            //jLabelInfoException.setText("IOException");
        }
    }//GEN-LAST:event_formInternalFrameClosed

    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // metodo que lee el fichero al abrir la ventana
        try {
            DataInputStream dis = new DataInputStream(new FileInputStream("f:\\testdata\\clientes.txt"));
            // mientras queden bytes por leer entro
            while (dis.available() != 0) {
                Cliente c = new Cliente();
                c.setCodigo(dis.readUTF().toString());
                c.setDni(dis.readUTF().toString());
                c.setNombre(dis.readUTF().toString());
                c.setApellidos(dis.readUTF().toString());
                c.setEstadoCivil(Integer.parseInt(dis.readUTF().toString()));
                c.setSocio(Boolean.parseBoolean(dis.readUTF().toString()));
                c.setDireccion(dis.readUTF().toString());
                c.setTelefonoFijo(Integer.parseInt(dis.readUTF().toString()));
                c.setTelefonoMovil(Integer.parseInt(dis.readUTF().toString()));
                c.setEdad(Integer.parseInt(dis.readUTF().toString()));
                // lee el salto de linea que separa clientes y no hace nada con el
                dis.readUTF();
                listaClientes.addCliente(c);
            }
            dis.close();
            // muestro el primer cliente al abrir el internal frame de clientes
            this.jButtonPrimeroActionPerformed(null);
        } catch (EOFException ex) {
            v.setMensaje("Error por fin de fichero", ex);
            //jLabelInfoException.setText("EOFException");
        } catch (FileNotFoundException ex) {
            v.setMensaje("Fichero no encontrado", ex);
            //jLabelInfoException.setText("FileNotFoundException");
        } catch (IOException ex) {
            v.setMensaje("Error de entrada salida", ex);
            //jLabelInfoException.setText("IOException");
        }
    }//GEN-LAST:event_formInternalFrameOpened

