    private void limpiarFormulario() {
        // metodo para limpiar formulario
        jTextFieldCodigo.setText("");
        jTextFieldDni.setText("");
        jTextFieldNombre.setText("");
        jTextFieldApellidos.setText("");
        jComboBoxEstadoCivil.setSelectedIndex(-1);
        jTextFieldDireccion.setText("");
        jCheckBoxSocio.setSelected(false);
        jTextFieldTelefonoFijo.setText("");
        jTextFieldTelefonoMovil.setText("");
        jTextFieldEdad.setText("");
    }

    private void guardarDatos(Cliente c) {
        // metodo para leer datos del formulario y setearlos en un cliente que le pasamos
        try {
            c.setCodigo(jTextFieldCodigo.getText());
            c.setDni(jTextFieldDni.getText());
            c.setNombre(jTextFieldNombre.getText());
            c.setApellidos(jTextFieldApellidos.getText());
            c.setEstadoCivil(jComboBoxEstadoCivil.getSelectedIndex());
            c.setDireccion(jTextFieldDireccion.getText());
            c.setSocio(jCheckBoxSocio.isSelected());
            c.setTelefonoFijo(Integer.parseInt(jTextFieldTelefonoFijo.getText()));
            c.setTelefonoMovil(Integer.parseInt(jTextFieldTelefonoMovil.getText()));
            c.setEdad(Integer.parseInt(jTextFieldEdad.getText()));
            listaClientes.addCliente(c);
            this.limpiarFormulario();
        } catch (NumberFormatException ex) {
            v.setMensaje("Formato incorrecto", ex);
        }
    }

    private void recuperarDatos(Cliente c) {
        // metodo para escribir en el formulario los datos de un cliente que le pasamos
        jTextFieldCodigo.setText(c.getCodigo());
        jTextFieldDni.setText(c.getDni());
        jTextFieldNombre.setText(c.getNombre());
        jTextFieldApellidos.setText(c.getApellidos());
        jTextFieldDireccion.setText(c.getDireccion());
        jCheckBoxSocio.setSelected(c.isSocio());
        jTextFieldTelefonoFijo.setText(Integer.toString(c.getTelefonoFijo()));
        jTextFieldTelefonoMovil.setText(Integer.toString(c.getTelefonoMovil()));
        jTextFieldEdad.setText(Integer.toString(c.getEdad()));
        jComboBoxEstadoCivil.setSelectedIndex(c.getEstadoCivil());
    }

